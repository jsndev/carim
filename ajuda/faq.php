<strong><u>GLOSSÁRIO</u>
</strong><br><br>
<strong>•	Administradora condominial <br>
</strong>Empresa de prestação de serviços voltada para administração de imóveis em condomínio. Entre os serviços oferecidos, destacam-se a assessoria ao síndico do condomínio em todos os aspectos legais e administrativos, tais como cobranças de despesas condominiais, pagamentos de despesas do condomínio, administração de pessoal etc.
<br><br>
<strong>•	Agente Fiduciário <br>
</strong>Qualquer empresa credenciada pelo Banco Central para, entre outras funções, promover a execução extrajudicial de empréstimos hipotecários vinculados ao SFH ou SFI. 
<br><br>
<strong>•	Agente financeiro <br>
</strong>Instituição pública ou privada que faz parte do Sistema Financeiro Nacional. Sua função é coletar, intermediar e aplicar recursos financeiros seus ou de outros, com autorização do Banco Central do Brasil.
<br><br>
<strong>•	Avaliação <br>
</strong>Processo no qual um perito determina o valor de um bem que irá ser dado em garantia do Credor.  
<br><br>
<strong>•	Cartório de Registro de Imóveis <br>
</strong>Órgão no qual são cadastrados todos os imóveis de determinada região. 
<br><br>
<strong>•	CND - Certidão Negativa de Débito<br>
</strong>Certidão que declara a existência ou não de débitos junto ao órgão expedidor. 
<br><br>
<strong>•	Certidão Quinzenária <br>
</strong>Documento fornecido pelo Cartório de Registro de Imóveis, em que consta o histórico do imóvel nos últimos quinze anos. Todas as transações realizadas com o imóvel estão nela contidas.
<br><br>
<strong>•	Correção Monetária<br>
</strong>Mecanismo para atualização do capital em decorrência da inflação medida em um determinado período. Os financiamentos imobiliários têm sua correção monetária determinada pela variação do índice TR utilizado na poupança.
<br><br>
<strong>•	Crédito Imobiliário <br>
</strong>Empréstimo concedido por instituições financeiras para custear a construção, reforma ou a compra de um imóvel, seja ele residencial ou comercial. 
<br><br>
<strong>•	Credor Fiduciário<br>
</strong>Trata-se da pessoa ou instituição financeira que concede empréstimo atrelado a uma garantia. 
<br><br>
<strong>•	Distribuidor Cível<br>
</strong>	Órgão responsável pela expedição de certidões, que apontam ou ações de natureza cível, criminal, trabalhista, entre outras, em nome de determinada pessoa, física ou jurídica.
<br><br>
<strong>•	Habite-se<br>
</strong>
	Documento fornecido ao proprietário do imóvel pelas autoridades públicas competentes que configura uma autorização para o uso do imóvel, em face de ter a construção observada os requisitos legais.
<br><br>
<strong>•	Inalienabilidade <br>
</strong>Impossibilidade de alienar o imóvel durante determinado período de tempo, em função de imposições contratuais ou legais. 
<br><br>
<strong>•	IPTU <br>
</strong>Imposto Predial e Territorial Urbano cobrado pela Prefeitura do local do imóvel.
<br><br>
<strong>•	ITBI ou Transmissão “inter vivos” <br>
</strong>Imposto sobre a Transmissão de Bens Imóveis. É o imposto que deve ser pago para a prefeitura, sempre que ocorrer a transmissão da propriedade de um bem imóvel, ou dos direitos reais sobre ele.
<br><br>
<strong>•	Laudêmio<br>
</strong>Valor pago ao Senhorio Direto do Imóvel, que pode ser a União, outro órgão ou pessoa física , quando o ocupante ou o foreiro de imóvel localizado em sua propriedade, transfere os direitos de ocupação ou de foro a outrem. 
<br><br>
<strong>•	Leilão<br>
</strong>Venda pública de bens penhorados a quem oferecer maior lance.
<br><br>
<strong>•	Mutuário <br>
</strong>Também chamado de devedor, trata-se de pessoa que recebe um empréstimo.
<br><br>
<strong>•	Pacto Antenupcial<br>
</strong>	Convenção por escritura pública na qual os noivos firmam antes da celebração do casamento, dispondo sobre o regime matrimonial de bens e outras relações de natureza econômica. 
<br><br>
<strong>•	Parte Ideal<br>
</strong>Fração que sócios ou condôminos possuem de um imóvel comum.
<br><br>
<strong>•	Penhora<br>
</strong>Apreensão judicial de bens de um devedor que não saldou os seus compromissos.
<br><br>
<strong>•	Proponente <br>
</strong>Pessoa que solicita um financiamento em uma Instituição Financeira. 
<br><br>
<strong>•	Protesto <br>
</strong>É o ato promovido perante o Cartório de Protestos de títulos, motivado pela falta de pagamento de um título ou dívida.
<br><br>
<strong>•	Título Aquisitivo<br>
</strong>Normalmente é a Escritura de Compra e Venda, na qual se regula a transmissão/transferência do imóvel e as partes envolvidas. O Contrato de financiamento tem força de Escritura Pública. 
<br><br>
<strong>•	Usufruto <br>
</strong>É um direito concedido a alguém, com necessário registro em cartório, no qual a pessoa (usufrutuária) pode usar um bem que não é de sua propriedade, usufruindo os frutos, ou seja, aquilo que esse bem produz (no caso de bem imóvel, o aluguel, por exemplo).
<br><br>
<strong>•	Valor venal <br>
</strong>Valor atribuído pela Prefeitura Municipal a cada imóvel, levando em conta sua metragem, localização, destinação e características. Literalmente, valor venal significa valor de venda. É com base no valor venal do imóvel que se calcula o valor do IPTU. 
<br><br>
<strong>•	Vistoria <br>
</strong>Trata-se de uma Inspeção feita pela Prefeitura Municipal, para verificar se o prédio urbano está conforme o projeto aprovado inicialmente. Este termo é também utilizado para designar as inspeções que os peritos efetuam as obras.
